def corr_coef( x, y ):
    #print( x.shape, y.shape )
    x_bar = np.mean( x )
    y_bar = np.mean( y )

    n, d1, d2 = 0., 0., 0.
    for i in range( 50 ):
        n += ( x[i] - x_bar ) * ( y[i] - y_bar )
        d1 += ( x[i] - x_bar )**2
        d2 += ( y[i] - y_bar )**2
    return n / ( ( d1 * d2 )**(0.5) )

